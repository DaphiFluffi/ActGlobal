package actGlobal;

public class Organisator {

	private String email;
	private String nachname;
	private String vorname;
	private String passwort;

	public Organisator() {

	}

	public String getEmail() {
		return email;
	}

	public String setEmail(String email) {
		return this.email = email;
	}

	public String getNachname() {
		return nachname;
	}

	public String setNachname(String nachname) {
		return this.nachname = nachname;
	}

	public String getVorname() {
		return vorname;
	}

	public String setVorname(String vorname) {
		return this.vorname = vorname;
	}

	public String getPasswort() {
		return passwort;
	}

	public String setPasswort(String passwort) {
		return this.passwort = passwort;
	}

}